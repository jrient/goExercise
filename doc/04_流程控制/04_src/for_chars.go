package main

import "fmt"

func main() {

	// 方法1 双重循环
	for i:=0; i<25; i++ {
		for j:=0; j<i; j++ {
			fmt.Printf("G")
		}
		fmt.Printf("\n")
	}


	// 方法2 单循环累加字符串
	str:="G"
	for i:=0; i<25; i++ {
		fmt.Println(str)
		str += "G"
	}
}

